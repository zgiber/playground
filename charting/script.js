function createLineChartOptions(data, colours) {
  let datasets = [];
  for (let i = 0; i < data.length; i++) {
    datasets.push({
      stepped: "middle",
      label: data[i].label,
      borderWidth: 2,
      pointRadius: 0,
      data: data[i].data,
      borderColor: colours[i % colours.length],
    });
  }


  return {
    type: "line",
    options: {
      responsive: true,
      // devicePixelRatio: 5,
      animation: false,
      scales: {
        x: {
          ticks: {
            // source: 'labels',
          },
          type: "timeseries",
          distribution: "linear",
        },

        y: {
          beginAtZero: true,
          ticks: {
            font: { size: 8 },
          },
        },
      },
      plugins: {
        legend: {
          position: "bottom",
          labels: {
            usePointStyle: true,
            align: "start",
            pointStyle: "line",
            padding: 10,
            font: { size: 12 },
          },
        },
      },
    },
    data: {
      datasets: datasets,
      // labels: ['2017-01-01','2018-01-01','2019-01-01','2020-01-01','2021-01-01','2022-01-01', ]
    },
  };
}

